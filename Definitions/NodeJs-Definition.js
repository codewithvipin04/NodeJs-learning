/* What is NodeJs ?
  NodeJS is an open-source, cross platform, Javascript runtime environment. 
   Open source - publicly available for sharing and modification.
   Cross platform - Available on Mac, Windows and Linux.
   Javascript runtime environment - provides all the necessary components in order to use and 
   run javascript program outside the browser.

  What can you build with NodeJs ?
    1. Traditional websites.
    2. Backend services like Api's.
    3. Real-time applications.
    4. Streaming services like Netflix.
    5. CLI Tools.
    6. Multiplayer games.

  NodeJs allow you to build complex and Powerful application.

  Summary: 
    1. It's not a language, it's not a framework.
    2. capable of executing javascript code outside the browser.

  Node REPL: Read, Evaluate, Execute, Print, Loop. 

  Browser vs NodeJs:
    1. in the browser, most of the time what you are doing interaction with the DOM, 
       or other web platform Api like Cookies. you don't have document window and all 
       other objects that provided by the browser.
    2. in the browser, we don't have all the nice Apis that NodeJs provides through it's
       modules, for exp - file system access functionality.
    3. with NodeJs you control the environment.
    4. with a browser, you are at the mercy of what users choose.   
*/
